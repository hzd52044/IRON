/*******************************************************************************
 * 函数名称:
 * 函数功能:
 * 入口参数:
 * 出口参数:
 * 注意事项:
*******************************************************************************/
void SdCmd(unsigned char Command)       //S8UC
{ 
    unsigned char i;
    
    LcdCSLow;
		LcdA0Low;
    for(i = 0; i < 8; i++)    
    {
				LcdCLKLow;
        if(Command & (0x80 >> i))
						LcdSDAHigh;
        else
            LcdSDALow;
        LcdCLKHigh;
    }
		LcdA0High;	//CD总线复位
		LcdA0Low;
    LcdCSHigh;
}
/*******************************************************************************
 * 函数名称:
 * 函数功能:
 * 入口参数:
 * 出口参数:
 * 注意事项:
*******************************************************************************/
void SdData(unsigned char Ddata)
{  
    unsigned char i;
	
    LcdCSLow;
		LcdA0High;
    for(i = 0; i < 8; i++)    
    {
        LcdCLKLow;
        if(Ddata & (0x80 >> i))
            LcdSDAHigh;
        else
            LcdSDALow;
        LcdCLKHigh;     
    }   
    LcdA0Low;
		LcdCSHigh;
}
