unsigned char hex2dec(unsigned long dat)
{
    //dat = 1111 ;   tmp_len = 4;       tmp_int [1] = 1;   [2] = 1 ;  [3]=1; [4]= 1
    unsigned char i;

    for (i=0;i<8;i++) tmp_int[i]=0;
    if (dat>=10000000)  tmp_len=8;
    else if (dat>=1000000)  tmp_len=7;
    else if (dat>=100000)  tmp_len=6;
    else if (dat>=10000)  tmp_len=5;
    else if (dat>=1000)  tmp_len=4; 
    else if (dat>=100)   tmp_len=3;
    else if (dat>=10)    tmp_len=2;
    else  tmp_len=1;
    if (dat>=10000000) {tmp_int[0]=dat/10000000; dat=dat%10000000;};
    if (dat>=1000000) {tmp_int[1]=dat/1000000; dat=dat%1000000;};
    if (dat>=100000) {tmp_int[2]=dat/100000; dat=dat%100000;};
    if (dat>=10000) {tmp_int[3]=dat/10000; dat=dat%10000;};
    if (dat>=1000)  {tmp_int[4]=dat/1000;  dat=dat%1000;};  // 111
    if (dat>=100)   {tmp_int[5]=dat/100;    dat=dat%100;}; // 11
    if (dat>=10)     {tmp_int[6]=dat/10;     dat=dat%10;}; // 1
    tmp_int[7]=dat%10;      // 1

    return tmp_len;
}     

void hextohex(unsigned int dat)
{
    tmp_string[0]=dat/4096;
    dat=dat%4096;
    tmp_string[1]=dat/256;
    dat=dat%256;
    tmp_string[2]=dat/16;
    dat=dat%16;
    tmp_string[3]=dat;
    tmp_len=4;
} 

unsigned char bcd_to_hex(unsigned char data)
{
    unsigned char temp;

    temp = ((data>>4)*10 + (data&0x0f));
    return temp;
}


